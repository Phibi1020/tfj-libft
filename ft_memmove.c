#include "libft.h"

void	*ft_memmove(void *dest, void *src, size_t n)
{
	char	*tmp;

	if (!dest && !src)
		return (0);
	tmp = (char *)malloc(sizeof(char) * n);
	if (tmp == NULL)
		return (NULL);
	ft_memcpy(tmp, src, n);
	ft_memcpy(dest, tmp, n);
	free(tmp);
	return (dest);
}
